package vo;

public class ScoreAnalysis {
	private int totalNum;
	private int notpassNum;
	private int passNum;
	private int mediumNum;
	private int goodNum;
	private int excellentNum;
	private String passRate;
	private int topScore;
	private int lowScore;
	private double averageScore;
	public int getTotalNum() {
		return totalNum;
	}
	public void setTotalNum(int totalNum) {
		this.totalNum = totalNum;
	}
	public int getNotpassNum() {
		return notpassNum;
	}
	public void setNotpassNum(int notpassNum) {
		this.notpassNum = notpassNum;
	}
	public int getPassNum() {
		return passNum;
	}
	public void setPassNum(int passNum) {
		this.passNum = passNum;
	}
	public int getMediumNum() {
		return mediumNum;
	}
	public void setMediumNum(int mediumNum) {
		this.mediumNum = mediumNum;
	}
	public int getGoodNum() {
		return goodNum;
	}
	public void setGoodNum(int goodNum) {
		this.goodNum = goodNum;
	}
	public int getExcellentNum() {
		return excellentNum;
	}
	public void setExcellentNum(int excellentNum) {
		this.excellentNum = excellentNum;
	}
	
	public String getPassRate() {
		return passRate;
	}
	public void setPassRate(String passRate) {
		this.passRate = passRate;
	}
	public int getTopScore() {
		return topScore;
	}
	public void setTopScore(int topScore) {
		this.topScore = topScore;
	}
	public int getLowScore() {
		return lowScore;
	}
	public void setLowScore(int lowScore) {
		this.lowScore = lowScore;
	}
	public double getAverageScore() {
		return averageScore;
	}
	public void setAverageScore(double averageScore) {
		this.averageScore = averageScore;
	}
	
	

}
